let day = prompt("Enter any day of week!!!").toLowerCase();

if (day === 'friday') {
    console.log("That's the best day")
}
else if (day === 'monday') {
    console.log('No one likes monday')
}
else if (day === 'saturday') {
    console.log('That is a weekend')
}
else {
    console.log('burrrrrr!!!!!')
}